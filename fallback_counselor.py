"""
임시 성경 데이터 없이도 작동하는 기본 응답 생성기
"""

def create_fallback_counseling_response(user_message: str) -> str:
    """성경 데이터 없이도 기본 상담 응답 생성"""
    
    # 키워드 기반 기본 응답
    message_lower = user_message.lower()
    
    if any(word in message_lower for word in ['가족', '부모', '자녀', '형제', '자매']):
        return """🙏 가족 관계로 고민하고 계시는군요.

가족은 하나님께서 주신 소중한 선물입니다. 비록 지금은 어려움이 있지만, 서로를 이해하고 용서하는 마음으로 다가가보세요.

"서로 친절하게 하며 불쌍히 여기며 서로 용서하기를 하나님이 그리스도 안에서 너희를 용서하심과 같이 하라" (에베소서 4:32)

기도와 함께 대화의 시간을 가져보시기 바랍니다."""

    elif any(word in message_lower for word in ['진로', '직장', '취업', '공부', '학교']):
        return """🙏 진로와 미래에 대해 고민하고 계시는군요.

하나님께서는 우리 각자에게 고유한 계획과 목적을 가지고 계십니다. 기도하며 지혜를 구하시고, 주변의 조언도 들어보세요.

"너의 행사를 여호와께 맡기라 그리하면 네가 경영하는 것이 이루어지리라" (잠언 16:3)

작은 한 걸음씩 내딛으며 하나님께서 인도하시는 길을 따라가시기 바랍니다."""

    elif any(word in message_lower for word in ['우울', '슬픔', '힘들', '괴로', '아프']):
        return """🙏 마음이 많이 힘드시군요.

하나님께서는 우리의 모든 눈물과 아픔을 아시고 계십니다. 혼자가 아니시니 두려워하지 마세요.

"수고하고 무거운 짐 진 자들아 다 내게로 오라 내가 너희를 쉬게 하리라" (마태복음 11:28)

필요하다면 가까운 사람들이나 전문가의 도움을 받으시고, 무엇보다 기도로 하나님께 맡겨보세요."""

    elif any(word in message_lower for word in ['기도', '부탁', '간구', '도움']):
        return """🙏 기도 요청을 받았습니다.

하나님께서 당신의 마음을 아시고, 가장 필요한 것으로 채워주시기를 기도합니다.

"너희 중에 두세 사람이 내 이름으로 모인 곳에는 나도 그들 중에 있느니라" (마태복음 18:20)

하나님의 평안과 은혜가 함께하시기를 축복합니다. 🕊️"""

    else:
        return f"""🙏 {user_message}로 고민하고 계시는군요.

모든 어려움 속에서도 하나님께서 함께하시며, 가장 좋은 길로 인도해 주실 것입니다.

"우리가 알거니와 하나님을 사랑하는 자 곧 그의 뜻대로 부르심을 입은 자들에게는 모든 것이 합력하여 선을 이루느니라" (로마서 8:28)

기도와 함께 지혜를 구하시며, 필요하다면 믿을 만한 분들과 상의해 보시기 바랍니다."""

